package Increment_Decrement;
public class Sample
{
     public static void main(String[] args)
     {
          int i = 0;
          System.out.println(i++); // 0
          int j = 1;
          System.out.println(j++); // 1
          int a = 2;
          System.out.println(a++); // 2
          int b = 0;
          System.out.println(++b); // 1
          int c = 7;
          System.out.println(--c); //6
          int d = 7;
          System.out.println(d--); //7
     }
}
